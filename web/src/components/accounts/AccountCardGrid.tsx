import { useRef, useMemo, useState, useEffect } from 'react'
import { useVirtualizer } from '@tanstack/react-virtual'
import { AccountCard } from './AccountCard'
import { Button } from '@/components/ui'
import type { AccountInfo, AccountMeta, AccountTag, AccountGroup, AccountActionState, CheckAccountResponse } from '@/types'
import { Plus } from 'lucide-react'

// 卡片高度（包含间距）
const CARD_HEIGHT = 340
// 卡片固定宽度
const CARD_WIDTH = 340
// 卡片间距
const GAP = 16

interface AccountCardGridProps {
  accounts: AccountInfo[]
  metaByName: Record<string, AccountMeta>
  groups: AccountGroup[]
  tags: AccountTag[]
  actionStates: Record<string, AccountActionState>
  selectedNames: Set<string>
  checkResults?: Record<string, CheckAccountResponse>
  onToggleSelection: (name: string) => void
  onRefresh: (name: string) => void
  onRefreshToken: (name: string) => void
  onRemove: (name: string) => void
  onViewDetails: (name: string) => void
  onAddAccount: () => void
  onCheck?: (name: string) => void
  onCopyCredentials?: (name: string) => void
}

export function AccountCardGrid({
  accounts,
  metaByName,
  groups,
  tags,
  actionStates,
  selectedNames,
  checkResults,
  onToggleSelection,
  onRefresh,
  onRefreshToken,
  onRemove,
  onViewDetails,
  onAddAccount,
  onCheck,
  onCopyCredentials
}: AccountCardGridProps) {
  const parentRef = useRef<HTMLDivElement>(null)
  const [columns, setColumns] = useState(3)

  // 根据容器宽度动态计算列数
  useEffect(() => {
    const container = parentRef.current
    if (!container) return

    const updateColumns = () => {
      const width = container.clientWidth
      // 计算能放下多少列：(宽度 + 间距) / (卡片宽度 + 间距)
      const cols = Math.max(1, Math.floor((width + GAP) / (CARD_WIDTH + GAP)))
      setColumns(cols)
    }

    updateColumns()

    const resizeObserver = new ResizeObserver(updateColumns)
    resizeObserver.observe(container)

    return () => resizeObserver.disconnect()
  }, [])

  // 将账号按行分组（包含添加按钮作为虚拟项）
  const rows = useMemo(() => {
    const result: (AccountInfo | 'add')[][] = []
    const allItems: (AccountInfo | 'add')[] = [...accounts, 'add']
    for (let i = 0; i < allItems.length; i += columns) {
      result.push(allItems.slice(i, i + columns))
    }
    return result
  }, [accounts, columns])

  const virtualizer = useVirtualizer({
    count: rows.length,
    getScrollElement: () => parentRef.current,
    estimateSize: () => CARD_HEIGHT,
    overscan: 2
  })

  const items = virtualizer.getVirtualItems()

  return (
    <div
      ref={parentRef}
      className="h-full overflow-auto"
      style={{ contain: 'strict' }}
    >
      <div
        style={{
          height: `${virtualizer.getTotalSize() + 8}px`,
          width: '100%',
          position: 'relative'
        }}
      >
        {items.map((virtualRow) => {
          const row = rows[virtualRow.index]

          return (
            <div
              key={virtualRow.key}
              style={{
                position: 'absolute',
                top: 0,
                left: 0,
                width: '100%',
                height: `${virtualRow.size}px`,
                transform: `translateY(${virtualRow.start + 8}px)` // +8px 为标签光环留空间
              }}
            >
              <div className="flex gap-4 px-2 items-start">
                {row.map((item) =>
                  item === 'add' ? (
                    <div
                      key="add-button"
                      className="flex items-center justify-center border-2 border-dashed border-muted-foreground/20 rounded-xl cursor-pointer hover:border-primary/50 hover:bg-primary/5 transition-colors flex-shrink-0"
                      style={{ width: CARD_WIDTH, height: CARD_HEIGHT - GAP }}
                      onClick={onAddAccount}
                    >
                      <div className="flex flex-col items-center gap-2 text-muted-foreground">
                        <Plus className="h-8 w-8" />
                        <span className="text-sm">添加账号</span>
                      </div>
                    </div>
                  ) : (
                    <div key={item.name} className="flex-shrink-0" style={{ width: CARD_WIDTH, height: CARD_HEIGHT - GAP }}>
                      <AccountCard
                        account={item}
                        meta={metaByName[item.name]}
                        groups={groups}
                        tags={tags}
                        actionState={actionStates[item.name] || 'idle'}
                        isSelected={selectedNames.has(item.name)}
                        checkResult={checkResults?.[item.name]}
                        onSelect={() => onToggleSelection(item.name)}
                        onRefresh={() => onRefresh(item.name)}
                        onRefreshToken={() => onRefreshToken(item.name)}
                        onRemove={() => onRemove(item.name)}
                        onViewDetails={() => onViewDetails(item.name)}
                        onCheck={onCheck ? () => onCheck(item.name) : undefined}
                        onCopyCredentials={onCopyCredentials ? () => onCopyCredentials(item.name) : undefined}
                      />
                    </div>
                  )
                )}
              </div>
            </div>
          )
        })}
      </div>

      {/* 空状态 */}
      {accounts.length === 0 && (
        <div className="absolute inset-0 flex items-center justify-center">
          <div className="text-center">
            <p className="text-muted-foreground mb-4">暂无账号</p>
            <Button onClick={onAddAccount}>
              <Plus className="h-4 w-4 mr-2" />
              添加第一个账号
            </Button>
          </div>
        </div>
      )}
    </div>
  )
}
